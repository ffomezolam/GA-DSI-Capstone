RE_WHITESPACE = re.compile(r'\s+')
RE_SENTENCE = re.compile(r'\w.*?[.?!]', re.S)

def extract_words(text, n=1):
    return RE_WORD.findall(text)

def extract_sentences(text):
    return RE_SENTENCE.findall(text)


